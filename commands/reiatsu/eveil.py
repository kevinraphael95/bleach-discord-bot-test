    async def _do_eveil(self, user_id: int, pouvoir: str) -> str:
        """V√©rifie la BDD et applique l‚Äô√©veil si possible."""

        # Connexion
        conn = sqlite3.connect("data/reiatsu.db")
        cursor = conn.cursor()

        # Cr√©ation table si elle n‚Äôexiste pas
        cursor.execute("""
            CREATE TABLE IF NOT EXISTS reiatsu2 (
                user_id INTEGER PRIMARY KEY,
                pouvoir TEXT
            )
        """)

        # V√©rifie le reiatsu actuel dans la table principale
        cursor.execute("SELECT reiatsu FROM reiatsu WHERE user_id = ?", (user_id,))
        row = cursor.fetchone()

        if not row:
            conn.close()
            return "‚ùå Tu n‚Äôas pas encore de reiatsu enregistr√©."

