        cursor.execute("INSERT OR REPLACE INTO reiatsu2 (user_id, pouvoir) VALUES (?, ?)", (user_id, pouvoir))
        conn.commit()
        conn.close()

        return f"üåå Tu as √©veill√© ton pouvoir spirituel : **{pouvoir}** ! (-300 reiatsu)"

